/**
 * Get proxy server logs
 * Usage: claude-flow proxy logs [--lines 100] [--follow]
 */
async function getProxyLogs(subArgs, flags) {
  printSuccess('üìÑ Getting OpenRouter proxy logs...');

  try {
    let cmd = 'npx agentic-flow proxy logs';

    if (flags.lines) {
      cmd += ` --lines ${flags.lines}`;
    }

    if (flags.follow || flags.f) {
      cmd += ' --follow';
    }

    if (flags.error) {
      cmd += ' --error';
    }

    const { stdout } = await execAsync(cmd, {
      timeout: flags.follow ? 0 : 30000, // No timeout for follow mode
      maxBuffer: 10 * 1024 * 1024,
    });

    console.log(stdout);
  } catch (error) {
    printError('‚ùå Failed to get proxy logs');
    console.error(error.message);
    process.exit(1);
  }
}

/**
 * Restart proxy server
 * Usage: claude-flow proxy restart
 */
async function restartProxy(subArgs, flags) {
  printWarning('üîÑ Restarting OpenRouter proxy server...');

  try {
    // Stop first
    await stopProxy(subArgs, { ...flags, quiet: true });

    // Wait a moment
    await new Promise((resolve) => setTimeout(resolve, 2000));

    // Start again
    await startProxy(subArgs, flags);

    printSuccess('‚úÖ Proxy server restarted successfully!');
  } catch (error) {
    printError('‚ùå Failed to restart proxy server');
    console.error(error.message);
    process.exit(1);
  }
}

